				<h2>About This Project</h2>
				<p><b>SSTP: Social Spiritual Transfer Protocol</b> is an interactive installation that uses the theme of Victorian era séances to explore modern concepts of virtual communication, surveillance, privacy, security, and reality. It is inspired by the practice of spiritualism in the Victorian era. Beginning in the mid 1850’s and occurring well into the early 20th century, spirit mediums and séance practice became very popular in Victorian Europe and America. During that time vast technological and scientific advances were being made, and people began questioning everything they knew, including their faith. The belief in the afterlife and earth-dwelling spirits lead to attempts at communication with the departed, and people were intrigued by this new possibility.</p>
				<p>In this project I electronically alter and update the physical technologies of Victorian era spirit communication. I am drawn to all kinds of technology, both old and new. When I encounter an object that was made by humans for humans, it inspires questions about the purpose of the object and the views and intentions of the designer of the object. In my practice, I like to juxtapose technologies that were created in response to similar situations, and let a dialog occur. I find this conversation to be particularly intriguing when one of the technologies is a fictional or implied one. </p>
				<p>I return, again and again to technologies created to aid in communication. Language, writing, signaling, code, telephones, texting and the internet are all methods and systems we have created to speed the transfer of ideas and information. In particular, the internet is a relatively new platform on which communication has exploded, inventing new problems and pitfalls. I am currently exploring the ways in which we behave and perform while online, when anonymity shields us from our actions, and we can imagine ourselves as anything or anyone.</p>
				<p><b>SSTP</b> is an installation that provides a physical and virtual stage for these performances. Hidden streaming webcams within the Victorian parlor installation act as one side of a conversation being held between persons in the physical space, and persons online. The website streams the video from the physical space, enabling virtual participants with voyeuristic agency, while also providing alternative methods of communication. Virtual visitors are able to send verbal messages to an early 19th Century telephone, and their chatroom conversation is conjured audibly by physical séance participants interfacing with the parlor’s séance table. More traditional haunting actions are also available through the web interface, flickering lights, knocking sounds, and gusting wind are more primitive languages offered.</p>
