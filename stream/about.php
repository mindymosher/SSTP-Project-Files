				<p>Mindy Mosher was born in 1981 in Brooklyn, NY. She is a multidisciplinary artist specializing in interactive media, who also works in digital and analog photography, printmaking, and performance.</p>
				<p>Mosher's work focuses on various subjects drawn from her personal life experience and observations. She is especially interested in online communication, and is currently exploring the effects and changes to human interaction that always being connected has wrought. Mosher's pieces frequently incorporate elements of popular iconography and media, as well as elements of humor.</p>
